def get_latest_checkpoint_for_operator(vol_txt: str, op: str) -> Optional[str]:
    """Retrieve the most recent checkpoint snapshot for an operator.

    Checkpoints are special snapshots with type="checkpoint" that contain
    compressed summaries of the last 50 conversation turns.
    """
    index = load_snapshot_index()

    if not index:
        # Fallback: parse volume text to find checkpoints
        blocks = split_snapshots(vol_txt)
        checkpoints = []
        for snap_id, start, end in blocks:
            meta_match = re.search(r'OPERATOR:\s*(\S+)', vol_txt[start:end])
            type_match = re.search(r'TYPE:\s*(\S+)', vol_txt[start:end])

            if meta_match and type_match:
                snap_op = meta_match.group(1)
                snap_type = type_match.group(1)
                if snap_op == op and snap_type == "checkpoint":
                    checkpoints.append((snap_id, vol_txt[start:end]))

        if checkpoints:
            # Return the most recent checkpoint
            checkpoints.sort(key=lambda x: x[0])
            return checkpoints[-1][1]
        return None

    # Use index for fast retrieval
    matching = [
        (snap_id, meta)
        for snap_id, meta in index.items()
        if meta.get("operator") == op and meta.get("type") == "checkpoint"
    ]

    if not matching:
        return None

    # Sort by snap_id and get most recent
    matching.sort(key=lambda x: x[0])
    snap_id, meta = matching[-1]

    # Read the checkpoint snapshot
    vol_bytes = read_volume_bytes(VOL_PATH)
    byte_start = meta.get("byte_start", 0)
    byte_end = meta.get("byte_end", len(vol_bytes))

    try:
        snapshot_bytes = vol_bytes[byte_start:byte_end]
        return snapshot_bytes.decode('utf-8', errors='replace')
    except Exception as e:
        print(f"[CHECKPOINT] Error reading checkpoint {snap_id}: {e}")
        return None


def get_recent_checkpoints_for_operator(vol_txt: str, op: str, count: int = 1) -> list[str]:
    """Retrieve the N most recent checkpoint snapshots for an operator.

    Args:
        vol_txt: Volume text content
        op: Operator name
        count: Number of checkpoints to retrieve (default: 1)

    Returns:
        List of checkpoint snapshot texts, ordered from oldest to newest
    """
    if count <= 0:
        return []

    index = load_snapshot_index()

    if not index:
        # Fallback: parse volume text to find checkpoints
        blocks = split_snapshots(vol_txt)
        checkpoints = []
        for snap_id, start, end in blocks:
            meta_match = re.search(r'OPERATOR:\s*(\S+)', vol_txt[start:end])
            type_match = re.search(r'TYPE:\s*(\S+)', vol_txt[start:end])

            if meta_match and type_match:
                snap_op = meta_match.group(1)
                snap_type = type_match.group(1)
                if snap_op == op and snap_type == "checkpoint":
                    checkpoints.append((snap_id, vol_txt[start:end]))

        if checkpoints:
            # Sort by snap_id and return N most recent
            checkpoints.sort(key=lambda x: x[0])
            return [snap for _, snap in checkpoints[-count:]]
        return []

    # Use index for fast retrieval
    matching = [
        (snap_id, meta)
        for snap_id, meta in index.items()
        if meta.get("operator") == op and meta.get("type") == "checkpoint"
    ]

    if not matching:
        return []

    # Sort by snap_id and get N most recent
    matching.sort(key=lambda x: x[0])
    recent_matches = matching[-count:]

    # Read the checkpoint snapshots
    vol_bytes = read_volume_bytes(VOL_PATH)
    checkpoints = []

    for snap_id, meta in recent_matches:
        byte_start = meta.get("byte_start", 0)
        byte_end = meta.get("byte_end", len(vol_bytes))

        try:
            snapshot_bytes = vol_bytes[byte_start:byte_end]
            checkpoint_text = snapshot_bytes.decode('utf-8', errors='replace')
            checkpoints.append(checkpoint_text)
        except Exception as e:
            print(f"[CHECKPOINT] Error reading checkpoint {snap_id}: {e}")

    return checkpoints
