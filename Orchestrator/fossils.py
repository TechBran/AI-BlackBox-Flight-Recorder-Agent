def fossil_operator(text: str) -> str:
    m = OP_RX.search(text or "")
    return (m.group(1).strip() if m else "")

def filter_by_operator(fossils: List[str], op: str) -> List[str]:
    if not fossils: return []
    keep = []
    for f in fossils:
        op_in_fossil = fossil_operator(f)
        if op_in_fossil == op:
            keep.append(f)
        elif INCLUDE_OTHERS and not op_in_fossil:
            keep.append(f)
    return keep

