def window_snapshot_text(text: str, best_ordinal, budget_chars: int,
                         model_key: Optional[str] = None,
                         content_mode: Optional[str] = None) -> str:
    """Deliver `text` within `budget_chars`, windowed on its best-matched chunk.

    best_ordinal is the store-collapse chunk identity from
    retrieve(return_provenance=True):
      - None / 0  -> no specific window (v1 row, keyword-only candidate, or the
        whole-doc vector won) -> today's head truncation (cap_chars).
      - >= 1      -> chunk (best_ordinal - 1) of the deterministic chunker
        output won: re-derive spans via chunk_snapshot(<source>, model_key) (the
        chunker guarantees verbatim substrings; a multi-chunk group stores the
        whole doc at ordinal 0 and chunker chunk i at ordinal i+1), locate the
        chunk's char range, and expand symmetrically to the budget, snapping
        to line boundaries where cheap.

    content_mode (M14.3c) picks WHAT the re-derivation chunks — it MUST match
    the store that produced best_ordinal, or every ordinal shifts by the
    envelope chunk count and the window points at the wrong passage:
      - None (default) -> resolve from the active store (the ordinals came from
        it); a corrupt/absent store degrades to "full".
      - "full" -> chunk the whole `text` (today's behavior, byte-identical).
      - "body" -> chunk extract_snapshot_content(text) (the Raw-Session-Log
        body — a SUFFIX of `text`), matching a body-mode store's stored chunks.
    Span-location + windowing stay on the FULL `text` either way: the body is a
    suffix, so text.find(body_chunk) still locates it and the START/END marker
    preservation below is unchanged.

    The snapshot's START marker line is always preserved (extract_snap_ids /
    fill_unseen parse provenance from it) and the END marker line is kept when
    present; elided regions are marked with "[…]". Output length <= budget_chars.
    Falls back to cap_chars on ANY re-derivation mismatch (chunker config drift
    since mint, ordinal out of range, span not found) — never raises.
    """
    if len(text) <= budget_chars:
        return text
    if not isinstance(best_ordinal, int) or best_ordinal < 1:
        return cap_chars(text, budget_chars)
    try:
        from Orchestrator.embeddings.chunker import chunk_snapshot  # lazy: keep fossils import-light
        if content_mode is None:
            content_mode = _resolve_active_content_mode()
        # Re-derive against the SAME text the store chunked (M14.3c). In body
        # mode the body is a suffix of `text`, so the located span still lives
        # inside `text` and all the full-text windowing below is unchanged.
        chunk_source = text
        if content_mode == "body":
            body = extract_snapshot_content(text)
            if body:
                chunk_source = body
        chunks = chunk_snapshot(chunk_source, model_key=model_key)
        idx = best_ordinal - 1  # group ordinal i+1 == chunker chunk i
        if len(chunks) <= 1 or idx >= len(chunks):
            return cap_chars(text, budget_chars)  # chunker drift since mint

        # Locate the winning chunk's char span. Chunks are verbatim substrings
        # in document order with strictly increasing start positions, so a
        # sequential find (each from the previous start + 1) pins the right
        # occurrence even when chunk text repeats.
        pos, search_from = -1, 0
        for j in range(idx + 1):
            pos = text.find(chunks[j], search_from)
            if pos == -1:
                return cap_chars(text, budget_chars)  # re-derivation mismatch
            search_from = pos + 1
        c_start, c_end = pos, pos + len(chunks[idx])

        lines = text.splitlines()
        start_line = lines[0] if lines else ""
        end_line = (
            lines[-1]
            if len(lines) > 1 and lines[-1].startswith("=== END SNAPSHOT")
            else ""
        )
        # Reserve frame overhead (marker lines + gap marks + join newlines) so
        # the assembled result stays airtight under budget_chars.
        overhead = (
            len(start_line) + len(end_line) + 2 * (len(_WINDOW_GAP_MARK) + 2) + 4
        )
        body_budget = budget_chars - overhead
        if body_budget <= 0:
            return cap_chars(text, budget_chars)

        span = c_end - c_start
        if span >= body_budget:
            w_start, w_end = c_start, c_start + body_budget
        else:
            pad = (body_budget - span) // 2
            w_start = max(0, c_start - pad)
            w_end = min(len(text), w_start + body_budget)
            w_start = max(0, w_end - body_budget)  # re-anchor when end-clipped

        # Snap to line boundaries where cheap (<=200 chars), never eating into
        # the matched chunk's span.
        if w_start > 0:
            nl = text.find("\n", w_start, min(w_start + 200, c_start))
            if nl != -1:
                w_start = nl + 1
        if w_end < len(text):
            nl = text.rfind("\n", max(w_end - 200, c_end), w_end)
            if nl != -1:
                w_end = nl

        parts = []
        if w_start > 0:
            parts.append(start_line)
            parts.append(_WINDOW_GAP_MARK)
        parts.append(text[w_start:w_end])
        if w_end < len(text):
            parts.append(_WINDOW_GAP_MARK)
            if end_line:
                parts.append(end_line)
        return "\n".join(parts)
    except Exception:
        # Never-raise delivery contract: degrade to head truncation.
        return cap_chars(text, budget_chars)


# ── M14: body-only extraction for RANKING (rerank passages + embed chunks) ────
# Every snapshot text leads with a fixed bookkeeping envelope
# (=== START SNAPSHOT … === / CROSS-FILE BEACON / VOLUME TRACKER / GAUGES) that
# is near-identical across the whole corpus and measurably sabotages ranking:
# reranking on body-only text lifted Vertex recall@10 0.654 -> 0.846 (+29%) /
# MRR 0.352 -> 0.495 (+41%) on 26 labeled same-corpus queries (2026-07-04), and
# the envelope also dilutes every embedding chunk (chunk-0 becomes a
# near-identical boilerplate vector). This helper returns the content-bearing
# region only — the user+AI turns and any distilled summary — so the reranker
# and (M14.3+) the embedder score the meat, not the bookkeeping.

# Content markers, most-specific first. "Raw Session Log" is the primary cut
# (the turns + any Release Notes/summary after them); "SNAPSHOT BODY" is the
# fallback (still drops BEACON/TRACKER/GAUGES, keeps Kernel Index/Provenance).
_CONTENT_MARKERS = ("Raw Session Log", "SNAPSHOT BODY")


def extract_snapshot_content(text: str) -> str:
    """Return the content-bearing portion of a snapshot for RANKING.

    Strips the leading bookkeeping envelope, returning from the first content
    marker onward:
      1. "Raw Session Log" — the user+AI turns plus any Release Notes/summary
         that follows them (dense, keyword-rich content) — the primary cut;
      2. else "SNAPSHOT BODY" — fallback: still drops the worst offenders
         (BEACON/TRACKER/GAUGES) though it keeps the Kernel Index / Context
         Provenance bookkeeping below it;
      3. else the full text unchanged — a snapshot (or non-snapshot text)
         without these markers is NEVER reduced to empty.

    Uses the FIRST occurrence of each marker and returns from the start of that
    marker's line (snapping to line start so an indented marker line still
    drops everything above it). Only the leading envelope is stripped — the
    content region is returned verbatim. Never raises; empty in -> empty out.
    """
    if not text:
        return text
    try:
        for marker in _CONTENT_MARKERS:
            idx = text.find(marker)  # FIRST occurrence
            if idx != -1:
                # Return from the start of the marker's own line. rfind("\n")
                # -> -1 when the marker is on the first line, so +1 yields 0.
                line_start = text.rfind("\n", 0, idx) + 1
                return text[line_start:]
        return text
    except Exception:
        # Never-raise contract: on any unexpected failure, degrade to the full
        # text (ranking on the envelope-inclusive text, never on nothing).
        return text


# ── M15: body-only formatting for DELIVERY (what the MODEL receives) ──────────
# extract_snapshot_content (above) strips for RANKING — it keeps ONLY the Raw
# Session Log region. DELIVERY keeps MORE: a compact attribution header
# ([SNAP-id · UTC date · operator: <op>]) so the model still knows WHICH
# snapshot each memory came from, plus the Context Provenance section (the
# fossil lineage), plus the content (Raw Session Log onward). It still DROPS the
# ~1,000-chars/snapshot bookkeeping envelope the model can't use
# (START / CROSS-FILE BEACON / VOLUME TRACKER / GAUGES / Kernel Index) — measured
# ~5,000 tokens (~8%) of a ~19-snapshot turn. Deliberately a DIFFERENT kept-set
# from the ranking extractor (different consumer, different needs).

# Compact attribution parser: the START line carries UTC date + SNAP-id; GAUGES
# carries OPERATOR. Both are best-effort — a missing/unparseable field is simply
# omitted (never fabricated, never raises).
_DELIVERY_START_RX = re.compile(
    r'^\s*===\s*START SNAPSHOT\s*[—-]\s*UTC\s*(?P<utc>.+?)\s*[—-]\s*'
    r'(?P<snap>SNAP-\d{8}-\d+).*?===\s*$',
    re.M,
)
# Horizontal-whitespace-only around the value: an empty "OPERATOR:" line must
# NOT let \s* swallow the newline and capture the next GAUGES line's value.
_DELIVERY_OPERATOR_RX = re.compile(r'^[ \t]*OPERATOR:[ \t]*(?P<op>\S.*?)[ \t]*$', re.M)
_PROVENANCE_MARKER = "Context Provenance"


def _delivery_attribution(text: str) -> Optional[str]:
    """Best-effort compact attribution header `[SNAP-id · <date> · operator: op]`.

    Returns None when the START line is absent (skip the header entirely). When
    the START line is present, the SNAP-id is always emitted; the date and
    operator are each included only if parseable (omit just the missing field).
    """
    m = _DELIVERY_START_RX.search(text)
    if not m:
        return None
    fields = [m.group("snap")]
    utc = (m.group("utc") or "").strip()
    if utc:
        fields.append(utc)
    op_m = _DELIVERY_OPERATOR_RX.search(text)
    if op_m:
        op = (op_m.group("op") or "").strip()
        if op:
            fields.append(f"operator: {op}")
    return "[" + " · ".join(fields) + "]"


def _delivery_provenance_section(text: str, body: str) -> str:
    """The `Context Provenance` section text, IF it sits above the body cut.

    `body` is a suffix of `text` (extract_snapshot_content returns text[k:]), so
    its start offset is len(text) - len(body). When Context Provenance is above
    that cut (the common Raw-Session-Log case) it is lifted verbatim; when it is
    at/below the cut (the SNAPSHOT-BODY fallback keeps it inside `body`) return
    "" so it is not duplicated. Absent marker -> "".
    """
    idx = text.find(_PROVENANCE_MARKER)
    if idx == -1:
        return ""
    prov_start = text.rfind("\n", 0, idx) + 1  # start of the marker's own line
    body_start = len(text) - len(body)
    if prov_start >= body_start:
        return ""  # already inside the delivered body — don't duplicate
    return text[prov_start:body_start].rstrip()


def format_snapshot_for_delivery(text: str) -> str:
    """Return a snapshot formatted for MODEL DELIVERY (context injection + the
    AI-invoked search tools).

    Emits, in order:
      1. a compact attribution header `[SNAP-id · <UTC date> · operator: <op>]`
         (best-effort; fields omitted individually when unparseable; skipped
         entirely when the START line is absent),
      2. the `Context Provenance` section (the fossil lineage), if present, and
      3. `extract_snapshot_content(text)` — the Raw Session Log onward (the
         user+AI turns plus any Release Notes).

    Drops the bookkeeping envelope the model can't use: START / CROSS-FILE
    BEACON / VOLUME TRACKER / GAUGES / Kernel Index.

    Robust: markers absent (nothing to strip) -> return `text` unchanged; empty
    in -> empty out; never raises. Distinct from extract_snapshot_content, which
    strips for RANKING and keeps ONLY from Raw Session Log.
    """
    if not text:
        return text
    try:
        body = extract_snapshot_content(text)
        # body == text means no leading envelope was stripped (no content marker,
        # or the marker is already the first line) — there is nothing to gain by
        # reformatting, and we never fabricate a header onto un-stripped text.
        if body == text:
            return text
        header = _delivery_attribution(text)
        provenance = _delivery_provenance_section(text, body)
        parts = [p for p in (header, provenance, body) if p]
        return "\n".join(parts)
    except Exception:
        # Never-raise delivery contract: degrade to the full text unchanged.
        return text


def _decode_scored_snapshots(scored, operator: str,
                             window_budget_chars: Optional[int] = None) -> List[str]:
    """Shared decode for the retrieve()-shim retrievers: byte-offset decode of
    the ranked snap_ids (same operator filter + bounds guard both shims have
    always applied), plus OPT-IN matched-chunk windowing (M8/WI-7a).

    `scored` rows are (snap_id, score) or, in provenance mode,
    (snap_id, score, best_ordinal). window_budget_chars=None (every cloud
    caller) delivers decoded texts UNCHANGED; a positive budget windows each
    over-budget text on its best-matched chunk via window_snapshot_text. The
    chunker's model_key is the ACTIVE slug (the ordinals came from the active
    store) — resolved once, config-read only, no provider/network call.
    """
    index = load_snapshot_index()
    vol_bytes = read_volume_bytes(VOL_PATH)

    model_key = None
    content_mode = "full"
    if window_budget_chars is not None:
        try:
            from Orchestrator.embeddings.store import get_active_slug
            model_key = get_active_slug()
        except Exception:
            model_key = None  # tokenization floor still windows correctly
        # Resolve the active store's content_mode ONCE (M14.3c) and thread it
        # to every window call — the ordinals came from this store, so the
        # windower must re-chunk the same (full | body) text. Cheap + never
        # raises (degrades to "full").
        content_mode = _resolve_active_content_mode()

    results = []
    for row in scored:
        snap_id = row[0]
        best_ordinal = row[2] if len(row) > 2 else None
        meta = index.get(snap_id)
        if not meta:
            continue
        if operator and operator != "system" and meta.get("operator") != operator:
            continue
        start = meta["byte_start"]
        end = meta["byte_end"]
        if start < len(vol_bytes) and end <= len(vol_bytes):
            text = vol_bytes[start:end].decode('utf-8', errors='replace')
            if window_budget_chars is not None:
                text = window_snapshot_text(
                    text, best_ordinal, window_budget_chars, model_key,
                    content_mode=content_mode,
                )
            results.append(text)
    return results


