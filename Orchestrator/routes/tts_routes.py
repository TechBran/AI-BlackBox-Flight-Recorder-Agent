def call_google_tts_synthesize(inp: GoogleSSMLIn) -> bytes:
    """Calls Google TTS API to synthesize speech."""
    if not GOOGLE_API_KEY:
        raise HTTPException(status_code=400, detail="GOOGLE_API_KEY not configured.")

    url = f"{GOOGLE_TTS_SYNTHESIZE_URL}?key={GOOGLE_API_KEY}"

    # --- FIX: Add languageCode to the voice object ---
    # Extract language code from the voice name (e.g., "en-US" from "en-US-Wavenet-F")
    lang_code = "-".join(inp.voice_name.split("-")[:2])
    if not lang_code:
        raise HTTPException(status_code=400, detail="Invalid voice name, could not determine language code.")

    # Validate and clamp pitch and speaking rate to Google's acceptable ranges
    # Pitch: -20.0 to +20.0 semitones
    # Speaking rate: 0.25 to 4.0
    pitch = max(-20.0, min(20.0, float(inp.pitch)))
    speaking_rate = max(0.25, min(4.0, float(inp.speaking_rate)))

    payload = {
        "voice": {
            "name": inp.voice_name,
            "languageCode": lang_code  # <-- THE FIX
        },
        "audioConfig": {
            "audioEncoding": "MP3",
            "speakingRate": speaking_rate,
            "pitch": pitch
        }
    }

    if inp.ssml:
        # Ensure SSML is wrapped in <speak> tags if not already
        ssml_text = inp.ssml.strip()
        if not ssml_text.startswith("<speak"):
            ssml_text = f"<speak>{ssml_text}</speak>"
        payload["input"] = {"ssml": ssml_text}
    elif inp.text:
        # Plain-text branch only — never touch the SSML branch above (would strip <speak>).
        payload["input"] = {"text": sanitize_for_speech(inp.text)}
    else:
        raise HTTPException(status_code=400, detail="Either 'text' or 'ssml' must be provided.")

    # DEBUG: Log the request payload (without API key)
    print(f"[GOOGLE TTS] Request payload: {json.dumps(payload, indent=2)}")

    try:
        response = requests.post(url, json=payload, timeout=400)
        response.raise_for_status()
        data = response.json()
        if "audioContent" in data:
            print(f"[GOOGLE TTS] Success! Audio generated.")
            return base64.b64decode(data["audioContent"])
        raise HTTPException(status_code=500, detail=f"Invalid response from Google TTS API: {data}")
    except requests.exceptions.RequestException as e:
        error_detail = str(e)
        full_error = None
        try:
            # Try to parse the error response from Google
            if hasattr(e, 'response') and e.response is not None:
                full_error = e.response.json()
                error_detail = full_error.get("error", {}).get("message", e.response.text)
                print(f"[GOOGLE TTS ERROR] Full error response: {json.dumps(full_error, indent=2)}")
        except Exception:
            pass # Keep the original error string
        print(f"[GOOGLE TTS ERROR] Failed: {error_detail}")
        raise HTTPException(status_code=e.response.status_code if hasattr(e, 'response') and e.response else 502, detail=f"Google TTS API error: {error_detail}")


