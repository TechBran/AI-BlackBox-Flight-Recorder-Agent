def _elevenlabs_stream_chunked(text: str, voice: str, model_id: str | None,
                               voice_settings: dict | None):
    """Yield ElevenLabs audio for `text`, splitting by the model's live char cap
    and streaming each chunk's audio (each chunk has its own idle timeout)."""
    from Orchestrator.config import ELEVENLABS_TTS_MODEL_DEFAULT
    from Orchestrator.elevenlabs import tts as el_tts
    cap = el_tts.max_chars_for(model_id or ELEVENLABS_TTS_MODEL_DEFAULT)
    pieces = [text] if len(text) <= cap else chunk_text_for_tts(text, max_chars=cap)
    for piece in pieces:
        yield from el_tts.synthesize_stream(piece, voice, model_id=model_id, voice_settings=voice_settings)


@app.post("/tts")
def tts_openai(body: dict = Body(...)):
    """Text-to-speech using OpenAI TTS API (default) or ElevenLabs.

    ElevenLabs voices (``voice`` prefixed ``elevenlabs:`` OR ``provider ==
    "elevenlabs"``) are handled FIRST and routed through the quality-first
    ``elevenlabs.tts.synthesize`` path -- the OpenAI code below is untouched for
    every non-ElevenLabs voice.

    By default, returns audio stream (for browser compatibility).
    Set return_json=true in body to get JSON with audio_url (for MCP/API use).
    """
    # Scrub non-spoken symbols (markdown asterisks, backticks, pipes, emoji, …) ONCE
    # up front so every provider branch below inherits speech-clean text.
    if isinstance(body, dict) and body.get("text"):
        body["text"] = sanitize_for_speech(body["text"])
    # --- ElevenLabs branch (checked before OpenAI so the OpenAI path is intact) ---
    if (body.get("voice") or "").startswith("elevenlabs:") or body.get("provider") == "elevenlabs":
        text = (body.get("text") or "").strip()
        if not text:
            raise HTTPException(400, "No text provided")
        voice = (body.get("voice") or "").strip()
        model_id = (body.get("model") or "").strip() or None  # None -> synthesize defaults to eleven_v3
        voice_settings = body.get("voice_settings")
        # Browser path: prime the first chunk so an IMMEDIATE failure (bad voice /
        # auth / un-downgradable plan rejection) still returns the structured
        # fallback, then stream the rest. Only a true MID-stream stall truncates.
        if not body.get("return_json"):
            import itertools
            gen = _elevenlabs_stream_chunked(text, voice, model_id, voice_settings)
            try:
                first = next(gen)
            except StopIteration:
                return {"status": "fallback", "detail": "ElevenLabs returned no audio"}
            except Exception as e:
                print(f"[ELEVENLABS] /tts synthesis failed: {e}")
                return {"status": "fallback", "detail": str(e)}
            return StreamingResponse(
                itertools.chain([first], gen),
                media_type="audio/mpeg",
            )

        # MCP/API path: write chunks to the file AS THEY ARRIVE, then return the URL.
        # The provider's per-chunk idle timeout (synthesize_stream) bounds this — no
        # total cap — so a long-but-progressing generation completes instead of being
        # cut at 60s.
        filename = f"{uuid.uuid4()}_tts.mp3"
        save_path = UPLOADS_DIR / filename
        size = 0
        try:
            with open(save_path, "wb") as f:
                for chunk in _elevenlabs_stream_chunked(text, voice, model_id, voice_settings):
                    f.write(chunk)
                    size += len(chunk)
        except Exception as e:
            print(f"[ELEVENLABS] /tts synthesis failed: {e}")
            try:
                save_path.unlink(missing_ok=True)
            except Exception:
                pass
            return {"status": "fallback", "detail": str(e)}
        if size == 0:
            try:
                save_path.unlink(missing_ok=True)
            except Exception:
                pass
            return {"status": "fallback", "detail": "ElevenLabs returned no audio"}
        return {
            "status": "success",
            "audio_url": f"/ui/uploads/{filename}",
            "voice": voice,
            "model": model_id or "eleven_v3",
            "format": "mp3",
            "size_bytes": size,
        }

    # --- Local (custom server) branch: route to the registered Kokoro/Speaches
    # /v1/audio/speech, checked BEFORE the OpenAI path (which rejects Kokoro voice
    # ids with an OpenAI-voice-enum 400). Triggered by a 'local:' voice prefix or
    # provider=='local'. Mirrors the /tts/batch local branch.
    if (body.get("voice") or "").startswith("local:") or body.get("provider") == "local":
        from Orchestrator.onboarding.custom_servers import resolve_audio
        resolved = resolve_audio("tts")
        if not resolved:
            return {"status": "fallback", "message": "no local text-to-speech model available"}
        _srv, _model = resolved
        _text = (body.get("text") or "").strip()
        if not _text:
            raise HTTPException(400, "No text provided")
        _voice = (body.get("voice") or "").strip()
        _bare = _voice.split(":", 1)[1] if _voice.startswith("local:") else (_voice or "af_heart")
        _fmt = (body.get("format") or "mp3").strip()
        _req = {"model": _model, "input": _text, "voice": _bare, "response_format": _fmt}
        _hdr = {"Content-Type": "application/json"}
        if _srv.get("api_key"):
            _hdr["Authorization"] = f"Bearer {_srv['api_key']}"
        try:
            _r = requests.post(f"{_srv['base_url']}/audio/speech", json=_req, headers=_hdr,
                               timeout=TTS_TIMEOUT / 1000.0)
        except Exception as e:
            return {"status": "fallback", "detail": str(e)}
        if _r.status_code != 200:
            return {"status": "fallback", "api_status": _r.status_code, "api_body": _r.text[:400]}
        _mime = {"mp3": "audio/mpeg", "opus": "audio/ogg", "wav": "audio/wav",
                 "flac": "audio/flac", "pcm": "audio/wav"}.get(_fmt, "audio/mpeg")
        if not body.get("return_json"):
            return StreamingResponse(iter([_r.content]), media_type=_mime)
        _ext = {"mp3": ".mp3", "opus": ".ogg", "wav": ".wav", "flac": ".flac", "pcm": ".wav"}.get(_fmt, ".mp3")
        _filename = f"{uuid.uuid4()}_tts{_ext}"
        (UPLOADS_DIR / _filename).write_bytes(_r.content)
        return {"status": "success", "audio_url": f"/ui/uploads/{_filename}",
                "voice": _bare, "model": _model, "format": _fmt, "size_bytes": len(_r.content)}

    # --- On-box Qwen3-TTS branch: route to the llama-swap /v1/audio/speech
    # proxy (body-model auto-routed to the qwen-tts member). Triggered by a
    # 'qwen:' voice prefix or provider=='qwen'. Checked BEFORE the OpenAI path
    # (which rejects Qwen voice tokens with an OpenAI-voice-enum 400). Mirrors
    # the local branch but targets the on-box stack (:9098). sanitize_for_speech
    # already applied up front (line 140).
    if (body.get("voice") or "").startswith("qwen:") or body.get("provider") == "qwen":
        from Orchestrator import qwen_tts
        _text = (body.get("text") or "").strip()
        if not _text:
            raise HTTPException(400, "No text provided")
        _voice = (body.get("voice") or "").strip()
        _bare = _voice.split(":", 1)[1] if _voice.startswith("qwen:") else _voice
        # The on-box Qwen member emits WAV/PCM only (M6 /v1/audio/speech 400s any
        # other response_format — there is no mp3/opus encoder). Always request
        # 'wav' (a proper RIFF container the browser plays directly); ignore any
        # client `format` hint since Qwen cannot honor mp3/opus.
        _fmt = "wav"
        try:
            _r = qwen_tts.synthesize(_bare, _text, response_format=_fmt)
        except Exception as e:
            return {"status": "fallback", "detail": str(e)}
        if _r.status_code != 200:
            return {"status": "fallback", "api_status": _r.status_code,
                    "api_body": _r.text[:400]}
        _mime = "audio/wav"
        if not body.get("return_json"):
            return StreamingResponse(iter([_r.content]), media_type=_mime)
        _filename = f"{uuid.uuid4()}_tts.wav"
        (UPLOADS_DIR / _filename).write_bytes(_r.content)
        return {"status": "success", "audio_url": f"/ui/uploads/{_filename}",
                "voice": _bare, "model": qwen_tts.QWEN_TTS_MODEL, "format": _fmt,
                "size_bytes": len(_r.content)}

    if AUDIO_ENGINE == "browser": return {"status": "fallback"}
    api_key = OPENAI_API_KEY.strip()
    if not api_key: return {"status": "fallback", "message": "OPENAI_API_KEY not configured"}
    text = (body.get("text") or "").strip()
    if not text: raise HTTPException(400, "No text provided")

    audio_format = (body.get("format") or TTS_FORMAT).strip()
    req = {
        "model": (body.get("model") or TTS_MODEL).strip(),
        "input": text,
        "voice": (body.get("voice") or TTS_VOICE).strip(),
        "response_format": audio_format
    }

    try:
        r = requests.post(OPENAI_TTS_URL, json=req, headers={"Authorization": f"Bearer {api_key}"}, timeout=TTS_TIMEOUT/1000.0)
    except Exception as e:
        return {"status":"fallback","detail":str(e)}

    if r.status_code != 200:
        return {"status":"fallback","api_status":r.status_code,"api_body":r.text}

    mime = "audio/mpeg" if audio_format=="mp3" else ("audio/ogg" if audio_format=="opus" else "audio/wav")

    # Default: return audio stream for browser compatibility
    # Only save to file if explicitly requesting JSON response (MCP/API use)
    if not body.get("return_json"):
        return StreamingResponse(iter([r.content]), media_type=mime)

    # Save to file and return URL (for MCP/API use when return_json=true)
    ext = ".mp3" if audio_format == "mp3" else (".ogg" if audio_format == "opus" else ".wav")
    filename = f"{uuid.uuid4()}_tts{ext}"
    save_path = UPLOADS_DIR / filename

    with open(save_path, "wb") as f:
        f.write(r.content)

    audio_url = f"/ui/uploads/{filename}"
    return {
        "status": "success",
        "audio_url": audio_url,
        "voice": req["voice"],
        "model": req["model"],
        "format": audio_format,
        "size_bytes": len(r.content)
    }


