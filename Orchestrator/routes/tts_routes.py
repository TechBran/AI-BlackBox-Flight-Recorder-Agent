@app.post("/stt/json")
async def stt_json(body: dict = Body(...)):
    """
    Speech-to-text from JSON with base64-encoded PCM16 audio.
    Used by Portal's Gemini Live to transcribe user audio (since Gemini doesn't provide user_transcript).

    Body:
        audio: base64-encoded PCM16 audio data
        sample_rate: sample rate (default 16000)
        format: 'pcm16' (default)
    """
    from Orchestrator.stt.file_transcribe import transcribe_bytes

    audio_b64 = body.get("audio")
    if not audio_b64:
        raise HTTPException(400, "Missing 'audio' field (base64 PCM16)")

    sample_rate = body.get("sample_rate", 16000)

    try:
        # Decode base64 to PCM16 bytes
        pcm_bytes = base64.b64decode(audio_b64)

        if len(pcm_bytes) < 100:
            # Too short to transcribe
            return {"text": ""}

        # Convert PCM16 to WAV format (Whisper needs proper audio container)
        wav_buffer = io.BytesIO()
        with wave.open(wav_buffer, 'wb') as wav_file:
            wav_file.setnchannels(1)  # Mono
            wav_file.setsampwidth(2)  # 16-bit
            wav_file.setframerate(sample_rate)
            wav_file.writeframes(pcm_bytes)

        wav_bytes = wav_buffer.getvalue()
        print(f"[STT/JSON] Converting {len(pcm_bytes)} bytes PCM16 -> {len(wav_bytes)} bytes WAV @ {sample_rate}Hz")

    except Exception as e:
        raise HTTPException(400, f"Failed to decode/convert audio: {e}")

    # Transcribe via resolved provider (OpenAI, Google, or ElevenLabs). Flat text
    # only — this path serves Gemini Live quick transcription; diarization (YAGNI
    # here) lives on /stt. An explicit provider in the body is honored.
    try:
        text = transcribe_bytes(
            wav_bytes, "audio/wav", provider=body.get("provider"), filename="audio.wav"
        )
    except RuntimeError as e:
        raise HTTPException(400, str(e))
    except Exception as e:
        raise HTTPException(502, f"STT upstream error: {e}")

    print(f"[STT/JSON] Transcription: '{text[:100]}...' ({len(text)} chars)")
    return {"text": text}


