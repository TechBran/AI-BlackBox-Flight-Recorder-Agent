@app.get("/api/last-response")
async def get_last_response(operator: str):
    """
    Get the most recent assistant response for the given operator from the latest snapshot.
    Used for recovering incomplete conversations after page reload/rotation.
    """
    try:
        # Load the snapshot index
        index = load_snapshot_index()

        # Filter snapshots for this operator and sort by timestamp (newest first)
        operator_snaps = {
            snap_id: meta for snap_id, meta in index.items()
            if meta.get("operator") == operator
        }

        if not operator_snaps:
            raise HTTPException(404, f"No snapshots found for operator: {operator}")

        # Get the most recent snapshot
        latest_snap_id = max(
            operator_snaps.keys(),
            key=lambda sid: operator_snaps[sid].get("timestamp", "")
        )
        latest_meta = operator_snaps[latest_snap_id]

        # Read the snapshot from volume file using byte offsets
        byte_start = latest_meta["byte_start"]
        byte_end = latest_meta["byte_end"]

        with open(VOL_PATH, 'r', encoding='utf-8') as f:
            f.seek(byte_start)
            snapshot_text = f.read(byte_end - byte_start)

        # Parse the Raw Session Log section to extract conversation turns
        # Format: - [N] YYYY-MM-DDTHH:MM:SS.sssZ operator=Name role: message
        log_section_match = re.search(
            r'Raw Session Log\n(.*?)\n(?:Release Notes|===\s+END\s+SNAPSHOT)',
            snapshot_text,
            re.DOTALL
        )

        if not log_section_match:
            raise HTTPException(404, "No conversation log found in snapshot")

        log_section = log_section_match.group(1)

        # Extract all conversation lines
        # Pattern: - [N] timestamp operator=Name role: message
        log_lines = re.findall(
            r'-\s+\[\d+\]\s+\S+\s+operator=\S+\s+(user|assistant):\s+(.+)',
            log_section
        )

        if not log_lines:
            raise HTTPException(404, "No conversation turns found in snapshot")

        # Find the last assistant message
        last_assistant_msg = None
        for role, message in reversed(log_lines):
            if role == "assistant":
                last_assistant_msg = message.strip()
                break

        if not last_assistant_msg:
            raise HTTPException(404, "No assistant response found in snapshot")

        return {
            "success": True,
            "operator": operator,
            "snapshot_id": latest_snap_id,
            "timestamp": latest_meta.get("timestamp"),
            "response": last_assistant_msg
        }

    except HTTPException:
        raise
    except Exception as e:
        print(f"[ERROR] Failed to retrieve last response: {e}")
        import traceback
        traceback.print_exc()
        raise HTTPException(500, f"Failed to retrieve last response: {str(e)}")

