@app.post("/generate/video")
async def generate_video(inp: GenIn):
    """Queue Veo 3.1 video generation task (text-to-video or image-to-video)

    Supports image-to-video via:
    - image: ImageData with base64 data and mime_type
    - image_url: URL path like /ui/uploads/image.png (will be converted to base64)
    """
    import base64
    from pathlib import Path

    try:
        # Prepare image data if provided
        image_data = None
        source_image_url = None

        # Option 1: Direct base64 image data
        if inp.image:
            image_data = {
                "data": inp.image.data,
                "mime_type": inp.image.mime_type
            }

        # Option 2: Image URL - convert to base64
        elif inp.image_url:
            source_image_url = inp.image_url

            # Clean URL - remove host prefix if present
            clean_url = inp.image_url
            if clean_url.startswith("http://") or clean_url.startswith("https://"):
                from urllib.parse import urlparse
                parsed = urlparse(clean_url)
                clean_url = parsed.path

            # Resolve to file path: /ui/uploads/file.png -> Portal/uploads/file.png
            if clean_url.startswith("/ui/"):
                relative_path = clean_url.replace("/ui/", "")
                file_path = Path("Portal") / relative_path

                if not file_path.exists():
                    raise HTTPException(status_code=404, detail=f"Image file not found: {clean_url}")

                # Read and encode file
                with open(file_path, "rb") as f:
                    image_bytes = f.read()
                image_b64 = base64.b64encode(image_bytes).decode()

                # Determine MIME type from extension
                ext = file_path.suffix.lower()
                mime_map = {
                    ".png": "image/png",
                    ".jpg": "image/jpeg",
                    ".jpeg": "image/jpeg",
                    ".gif": "image/gif",
                    ".webp": "image/webp"
                }
                mime_type = mime_map.get(ext, "image/jpeg")

                image_data = {
                    "data": image_b64,
                    "mime_type": mime_type
                }
                print(f"[VIDEO] Converted image_url to base64: {clean_url} ({len(image_bytes)} bytes, {mime_type})")
            else:
                raise HTTPException(status_code=400, detail=f"Invalid image_url format: {inp.image_url}")

        # Parse duration - support both 'duration' (int) and legacy 'durationSeconds' (str)
        duration = inp.duration or 8
        if inp.durationSeconds and not inp.duration:
            duration = int(inp.durationSeconds)

        # Store video options in result_data for the worker to use
        video_options = {
            "aspectRatio": inp.aspectRatio or "16:9",
            "resolution": inp.resolution or "720p",
            "duration": duration,
            "negativePrompt": inp.negativePrompt
        }

        # Store source image URL for reference if used
        if source_image_url:
            video_options["source_image_url"] = source_image_url

        task = create_task(
            TaskType.VIDEO_GENERATION,
            operator=inp.operator,
            prompt=inp.prompt,
            image_data=image_data,
            result_data=video_options  # Pass video options to worker
        )

        mode = "image-to-video" if image_data else "text-to-video"
        res_str = inp.resolution or "720p"
        return {
            "task_id": task.task_id,
            "status": task.status,
            "message": f"Video generation ({mode}, {duration}s @ {res_str}) queued (Veo 3.1 - may take 5-20 minutes)",
            "source_image": source_image_url
        }
    except HTTPException:
        raise
    except Exception as e:
        print(f"ERROR: Failed to queue video generation: {e}")
        raise HTTPException(status_code=500, detail=str(e))

