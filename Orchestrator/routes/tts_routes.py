def call_lyria_music(inp: LyriaMusicIn) -> list:
    """Generate music using Google Lyria-002 via Vertex AI.
    Returns list of WAV audio bytes (48kHz stereo, 30 seconds each).
    Supports generating 1-4 variations via sample_count parameter.
    Note: seed and sample_count > 1 are mutually exclusive per API spec.
    """
    if not USE_CLOUD_TTS or not GOOGLE_AUTH_AVAILABLE:
        raise HTTPException(
            status_code=400,
            detail="Lyria requires Google Cloud service account. Set GOOGLE_APPLICATION_CREDENTIALS."
        )

    # Get access token using same auth as Cloud TTS
    access_token = _get_cloud_tts_access_token()
    project_id = _get_lyria_project_id()

    if not project_id:
        raise HTTPException(status_code=400, detail="Could not determine project ID from service account")

    # Validate sample_count (1-4)
    sample_count = inp.sample_count if inp.sample_count else 1
    sample_count = max(1, min(4, sample_count))  # Clamp to 1-4

    # Validate mutual exclusivity: seed cannot be used with sample_count > 1
    if inp.seed is not None and sample_count > 1:
        raise HTTPException(
            status_code=400,
            detail="Cannot use seed with sample_count > 1. Use seed for reproducibility OR sample_count for variations, not both."
        )

    # Build the Lyria API URL
    url = LYRIA_MUSIC_URL.format(project_id=project_id)

    # Build request payload
    instance = {"prompt": inp.prompt}
    if inp.negative_prompt:
        instance["negative_prompt"] = inp.negative_prompt
    if inp.seed is not None:
        instance["seed"] = inp.seed

    payload = {
        "instances": [instance],
        "parameters": {
            "sample_count": sample_count
        }
    }

    headers = {
        "Authorization": f"Bearer {access_token}",
        "Content-Type": "application/json"
    }

    print(f"[Lyria] Generating {sample_count} music variation(s): {inp.prompt[:100]}...")
    print(f"[Lyria] URL: {url}")

    try:
        # Increase timeout for multiple samples
        timeout = 120 + (sample_count - 1) * 30  # Extra 30s per additional sample
        response = requests.post(url, headers=headers, json=payload, timeout=timeout)
        print(f"[Lyria] Response status: {response.status_code}")

        if response.status_code != 200:
            error_detail = response.text[:500] if response.text else "Unknown error"
            print(f"[Lyria] Error: {error_detail}")
            # Check for recitation filter
            if "recitation" in error_detail.lower():
                raise HTTPException(
                    status_code=400,
                    detail="Prompt blocked by copyright filter. Try a more original description that doesn't reference specific songs or artists."
                )
            raise HTTPException(status_code=response.status_code, detail=f"Lyria API error: {error_detail}")

        result = response.json()

        # Extract audio content from response
        predictions = result.get("predictions", [])
        if not predictions:
            raise HTTPException(status_code=500, detail="No audio generated by Lyria")

        # Extract all audio samples from predictions
        audio_samples = []
        for i, prediction in enumerate(predictions):
            audio_content_b64 = prediction.get("bytesBase64Encoded", "") or prediction.get("audioContent", "")
            if audio_content_b64:
                wav_bytes = base64.b64decode(audio_content_b64)
                audio_samples.append(wav_bytes)
                print(f"[Lyria] Sample {i+1}: {len(wav_bytes)} bytes")

        if not audio_samples:
            raise HTTPException(status_code=500, detail="No audio content in Lyria response")

        print(f"[Lyria] Generated {len(audio_samples)} audio sample(s)")
        return audio_samples

    except requests.exceptions.Timeout:
        raise HTTPException(status_code=504, detail="Lyria API timeout - music generation took too long")
    except requests.exceptions.RequestException as e:
        raise HTTPException(status_code=502, detail=f"Lyria API request failed: {e}")


