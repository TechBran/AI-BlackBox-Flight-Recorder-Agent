def pcm_to_wav(
    pcm_data: bytes,
    sample_width: int,
    frame_rate: int,
    num_channels: int
) -> bytes:
    """
    Converts raw PCM data to a WAV file in memory.
    This version assumes sample_width is 2 (16-bit) and does NOT use audioop.
    """
    # We expect 16-bit (2-byte) audio from Gemini TTS
    if sample_width != 2:
        raise ValueError(f"Expected 16-bit (2-byte) sample width for WAV conversion, but got {sample_width}")

    with io.BytesIO() as wav_buffer:
        with wave.open(wav_buffer, "wb") as wf:
            wf.setnchannels(num_channels)
            wf.setsampwidth(sample_width) # This will be 2
            wf.setframerate(frame_rate)
            wf.writeframes(pcm_data) # Use pcm_data directly
        wav_bytes = wav_buffer.getvalue()
    return wav_bytes


def chunk_text_for_tts(text: str, max_chars: int = 800) -> List[str]:
    """Split text into chunks at sentence boundaries, each under max_chars.

    Uses sentence-ending punctuation and paragraph breaks as split points.
    Falls back to word-level splitting for sentences longer than max_chars.
    """
    if not text or not text.strip():
        return []

    text = text.strip()
    if len(text) <= max_chars:
        return [text]

    # Split at sentence boundaries (.!?) or paragraph breaks
    sentences = re.split(r'(?<=[.!?])\s+|(?<=\n)\n', text)
    sentences = [s.strip() for s in sentences if s.strip()]

    chunks: List[str] = []
    current_chunk = ""

    for sentence in sentences:
        # If this single sentence exceeds max_chars, split at word level
        if len(sentence) > max_chars:
            # Flush current chunk first
            if current_chunk:
                chunks.append(current_chunk.strip())
                current_chunk = ""
            # Word-level splitting for long sentences
            words = sentence.split()
            word_chunk = ""
            for word in words:
                candidate = f"{word_chunk} {word}".strip() if word_chunk else word
                if len(candidate) <= max_chars:
                    word_chunk = candidate
                else:
                    if word_chunk:
                        chunks.append(word_chunk)
                    word_chunk = word
            if word_chunk:
                current_chunk = word_chunk
            continue

        # Try appending sentence to current chunk
        candidate = f"{current_chunk} {sentence}".strip() if current_chunk else sentence
        if len(candidate) <= max_chars:
            current_chunk = candidate
        else:
            # Current chunk is full, start a new one
            if current_chunk:
                chunks.append(current_chunk.strip())
            current_chunk = sentence

    # Don't forget the last chunk
    if current_chunk and current_chunk.strip():
        chunks.append(current_chunk.strip())

    return chunks


def stitch_wav_chunks(wav_chunks: List[bytes]) -> bytes:
    """Combine multiple WAV byte buffers into a single WAV file.

    Reads WAV params from the first chunk and concatenates raw PCM frames
    from all chunks. This avoids audio artifacts from naive byte concatenation.
    """
    if not wav_chunks:
        return b""
    if len(wav_chunks) == 1:
        return wav_chunks[0]

    # Read params from the first chunk
    first_buf = io.BytesIO(wav_chunks[0])
    with wave.open(first_buf, "rb") as wf:
        params = wf.getparams()
        all_frames = wf.readframes(wf.getnframes())

    # Read PCM frames from remaining chunks
    for chunk_bytes in wav_chunks[1:]:
        buf = io.BytesIO(chunk_bytes)
        with wave.open(buf, "rb") as wf:
            all_frames += wf.readframes(wf.getnframes())

    # Write combined WAV
    out_buf = io.BytesIO()
    with wave.open(out_buf, "wb") as wf:
        wf.setparams(params)
        wf.writeframes(all_frames)
    return out_buf.getvalue()


