@app.post("/tts/batch")
async def tts_batch(body: dict = Body(...)):
    """Batch TTS: split text into chunks, generate all in parallel, return combined audio.

    Accepts:
        text (str): Full text to synthesize
        provider (str): "openai", "gemini-pro", "gemini-flash", or "elevenlabs" (default: "openai")
        voice (str): Voice name (default: config TTS_VOICE)
        model (str): Model override (provider-specific)
        format (str): "mp3" or "wav" (default: config TTS_FORMAT)
        operator (str): Operator name for logging
        max_chunk_chars (int): Max characters per chunk (default: 800)
    """
    t_start = time.time()

    # Scrub non-spoken symbols before chunking so every chunk (any provider) is clean.
    text = sanitize_for_speech(body.get("text") or "")
    if not text:
        raise HTTPException(400, "No text provided")

    provider = (body.get("provider") or "openai").strip().lower()
    voice = (body.get("voice") or TTS_VOICE).strip()
    # A local:-prefixed voice forces the local provider (parity with POST /tts), so a
    # caller that passes the voice but omits provider still routes to Kokoro, not OpenAI.
    if voice.startswith("local:"):
        provider = "local"
    # A qwen:-prefixed voice forces the qwen provider (parity with local:), so a
    # caller that passes the prefixed voice but omits provider still routes on-box.
    if voice.startswith("qwen:"):
        provider = "qwen"
    model = (body.get("model") or "").strip()
    audio_format = (body.get("format") or TTS_FORMAT).strip()
    operator = (body.get("operator") or "unknown").strip()
    # Provider-specific chunk defaults: OpenAI is fast per-request, Gemini benefits from smaller chunks
    default_chunk = 1500 if provider == "openai" else 800
    max_chunk_chars = int(body.get("max_chunk_chars", default_chunk))

    # Split text into chunks
    chunks = chunk_text_for_tts(text, max_chars=max_chunk_chars)
    if not chunks:
        raise HTTPException(400, "Text produced no chunks after splitting")

    num_chunks = len(chunks)
    print(f"[TTS Batch] {provider} | {num_chunks} chunks | voice={voice} | format={audio_format} | operator={operator}")

    loop = asyncio.get_running_loop()

    # --- OpenAI provider ---
    if provider == "openai":
        api_key = OPENAI_API_KEY.strip()
        if not api_key:
            raise HTTPException(400, "OPENAI_API_KEY not configured")

        effective_model = model or TTS_MODEL

        def _openai_tts_chunk(chunk_text: str) -> bytes:
            req = {
                "model": effective_model,
                "input": chunk_text,
                "voice": voice,
                "response_format": audio_format,
            }
            r = requests.post(
                OPENAI_TTS_URL,
                json=req,
                headers={"Authorization": f"Bearer {api_key}"},
                timeout=TTS_TIMEOUT / 1000.0,
            )
            if r.status_code != 200:
                raise HTTPException(502, f"OpenAI TTS failed (HTTP {r.status_code}): {r.text[:200]}")
            return r.content

        # Run all chunks in parallel
        futures = [loop.run_in_executor(_tts_executor, _openai_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

    # --- Gemini provider ---
    elif provider in ("gemini-pro", "gemini-flash"):
        def _gemini_tts_chunk(chunk_text: str) -> bytes:
            inp = GeminiProTTSIn(
                text=chunk_text,
                voice_name=voice,
                model=model if model else None,
                operator=operator,
            )
            # call_gemini_tts returns WAV bytes
            return call_gemini_tts(inp)

        futures = [loop.run_in_executor(_tts_executor, _gemini_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

        # Gemini always returns WAV; override format for stitching logic
        audio_format = "wav"

    # --- ElevenLabs provider ---
    elif provider == "elevenlabs":
        from Orchestrator.config import ELEVENLABS_TTS_MODEL_DEFAULT
        from Orchestrator.elevenlabs import tts as el_tts

        # Re-chunk by the model's LIVE per-request cap (not the generic
        # max_chunk_chars used above) so we make the fewest synthesize calls.
        el_model = model or ELEVENLABS_TTS_MODEL_DEFAULT
        cap = el_tts.max_chars_for(el_model)
        chunks = chunk_text_for_tts(text, max_chars=cap)
        num_chunks = len(chunks)
        voice_settings = body.get("voice_settings")

        def _el_tts_chunk(chunk_text: str) -> bytes:
            return el_tts.synthesize(
                chunk_text, voice,
                model_id=model if model else None,
                voice_settings=voice_settings,
            )

        futures = [loop.run_in_executor(_tts_executor, _el_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

        # ElevenLabs returns MP3; concatenated frames play fine (no WAV stitch).
        audio_format = "mp3"

    # --- Local (custom server) provider: /v1/audio/speech, OpenAI-shaped ---
    elif provider == "local":
        from Orchestrator.onboarding.custom_servers import resolve_audio
        resolved = resolve_audio("tts")
        if not resolved:
            raise HTTPException(400, "No local text-to-speech model available (register an OpenAI-compatible server hosting a TTS model)")
        _srv, _model = resolved
        _bare_voice = voice.split(":", 1)[1] if voice.startswith("local:") else voice

        def _local_tts_chunk(chunk_text: str) -> bytes:
            req = {"model": _model, "input": chunk_text, "voice": _bare_voice,
                   "response_format": audio_format}
            headers = {"Content-Type": "application/json"}
            if _srv.get("api_key"):
                headers["Authorization"] = f"Bearer {_srv['api_key']}"
            r = requests.post(f"{_srv['base_url']}/audio/speech", json=req,
                              headers=headers, timeout=TTS_TIMEOUT / 1000.0)
            if r.status_code != 200:
                raise HTTPException(502, f"Local TTS failed (HTTP {r.status_code}): {r.text[:200]}")
            return r.content

        futures = [loop.run_in_executor(_tts_executor, _local_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

    # --- On-box Qwen3-TTS provider: llama-swap /v1/audio/speech, sequential ---
    elif provider == "qwen":
        from Orchestrator import qwen_tts
        _bare = voice.split(":", 1)[1] if voice.startswith("qwen:") else voice

        def _qwen_all() -> List[bytes]:
            # Sequential on purpose: one GPU serializes synthesis anyway, and
            # the qwen-tts member's concurrencyLimit:2 would 429 a parallel
            # fan-out. Any non-200 raises -> the endpoint returns 502.
            # Request WAV: the M6 server emits WAV/PCM only (it 400s mp3/opus —
            # there is no encoder), so audio_format cannot flow through here.
            out: List[bytes] = []
            for ch in chunks:
                r = qwen_tts.synthesize(_bare, ch, response_format="wav")
                if r.status_code != 200:
                    raise HTTPException(502, f"Qwen TTS failed (HTTP {r.status_code}): {r.text[:200]}")
                out.append(r.content)
            return out

        results = await loop.run_in_executor(_tts_executor, _qwen_all)
        # Qwen returns WAV — drive the WAV stitcher + audio/wav mime below
        # (mirrors the Gemini branch's `audio_format = "wav"`).
        audio_format = "wav"

    else:
        raise HTTPException(400, f"Unknown provider: {provider}. Use 'openai', 'gemini-pro', 'gemini-flash', 'elevenlabs', 'local', or 'qwen'.")

    # Check for errors in results
    errors = [(i, r) for i, r in enumerate(results) if isinstance(r, Exception)]
    if errors:
        err_idx, err = errors[0]
        print(f"[TTS Batch] ERROR on chunk {err_idx}/{num_chunks}: {err}")
        raise HTTPException(502, f"TTS failed on chunk {err_idx + 1}/{num_chunks}: {err}")

    audio_chunks: List[bytes] = list(results)  # type: ignore[arg-type]

    # Stitch results
    if audio_format == "wav":
        combined = stitch_wav_chunks(audio_chunks)
    else:
        # MP3/opus: simple byte concatenation (frames are self-delimiting)
        combined = b"".join(audio_chunks)

    elapsed = time.time() - t_start
    mime = "audio/mpeg" if audio_format == "mp3" else ("audio/ogg" if audio_format == "opus" else "audio/wav")
    print(f"[TTS Batch] Done: {num_chunks} chunks, {len(combined)} bytes, {elapsed:.2f}s | {mime}")

    return StreamingResponse(iter([combined]), media_type=mime)


@app.post("/tts/stitch")
async def tts_stitch(chunks: List[UploadFile] = File(...)):
    """Stitch multiple WAV file chunks into a single combined WAV file.

    Accepts multipart form data with one or more WAV file uploads.
    Uses proper WAV header/frame stitching to avoid audio artifacts.
    """
    if not chunks:
        raise HTTPException(400, "No files provided")

    wav_chunks: List[bytes] = []
    for f in chunks:
        data = await f.read()
        if not data:
            continue
        wav_chunks.append(data)

    if not wav_chunks:
        raise HTTPException(400, "All uploaded files were empty")

    print(f"[TTS Stitch] Stitching {len(wav_chunks)} WAV chunks")
    combined = stitch_wav_chunks(wav_chunks)
    print(f"[TTS Stitch] Result: {len(combined)} bytes")

    return StreamingResponse(iter([combined]), media_type="audio/wav")


