def call_gemini_tts_cloud(inp: GeminiProTTSIn) -> bytes:
    """Calls Gemini TTS via Cloud Text-to-Speech API (GA models).
    Uses service account authentication and the GA model names:
    - gemini-2.5-pro-tts (high quality)
    - gemini-2.5-flash-tts (lower latency)
    """
    access_token = _get_cloud_tts_access_token()

    # Use model from input, default to pro if not specified or invalid
    valid_models = ["gemini-2.5-pro-tts", "gemini-2.5-flash-tts"]
    model_name = inp.model if inp.model in valid_models else "gemini-2.5-pro-tts"

    if inp.multi_speaker and inp.speakers and len(inp.speakers) >= 2:
        # Multi-speaker mode - use multiSpeakerMarkup format per Google Cloud TTS API
        # Build speaker voice configs with correct field names
        speaker_voice_configs = []
        for speaker in inp.speakers[:2]:
            speaker_voice_configs.append({
                "speakerAlias": speaker.get("name", "Speaker"),
                "speakerId": speaker.get("voice", "Kore")
            })

        # Parse text to extract turns (format: "SpeakerName: dialogue text")
        turns = []
        lines = inp.text.strip().split('\n')
        for line in lines:
            line = line.strip()
            if not line:
                continue
            # Try to parse "Speaker: text" format
            if ':' in line:
                parts = line.split(':', 1)
                speaker_name = parts[0].strip()
                text_content = parts[1].strip() if len(parts) > 1 else ""
                # Match speaker name to configured speakers
                matched_speaker = None
                for sp in inp.speakers[:2]:
                    if sp.get("name", "").lower() == speaker_name.lower():
                        matched_speaker = sp.get("name")
                        break
                if matched_speaker and text_content:
                    turns.append({
                        "speaker": matched_speaker,
                        "text": text_content
                    })
                elif text_content:
                    # Default to first speaker if no match
                    turns.append({
                        "speaker": inp.speakers[0].get("name", "Speaker1"),
                        "text": line
                    })
            else:
                # No colon, assign to first speaker
                turns.append({
                    "speaker": inp.speakers[0].get("name", "Speaker1"),
                    "text": line
                })

        # Build the correct payload structure for multi-speaker
        payload = {
            "input": {
                "multiSpeakerMarkup": {
                    "turns": turns
                }
            },
            "voice": {
                "languageCode": "en-US",
                "modelName": model_name,
                "multiSpeakerVoiceConfig": {
                    "speakerVoiceConfigs": speaker_voice_configs
                }
            },
            "audioConfig": {
                "audioEncoding": "LINEAR16",
                "sampleRateHertz": 24000
            }
        }
        print(f"[Gemini TTS Cloud GA] Multi-speaker mode: {len(speaker_voice_configs)} speakers, {len(turns)} turns")
    else:
        # Single-speaker mode
        voice_name = inp.voice_name or "Kore"
        payload = {
            "input": {
                "text": inp.text
            },
            "voice": {
                "languageCode": "en-US",
                "name": voice_name,
                "modelName": model_name
            },
            "audioConfig": {
                "audioEncoding": "LINEAR16",
                "sampleRateHertz": 24000
            }
        }
        print(f"[Gemini TTS Cloud GA] Single-speaker mode: {voice_name} (model: {model_name})")

    headers = {
        "Authorization": f"Bearer {access_token}",
        "Content-Type": "application/json"
    }

    try:
        print(f"[Gemini TTS Cloud GA] Calling Cloud TTS API with model: {model_name}")
        print(f"[Gemini TTS Cloud GA] URL: {CLOUD_TTS_URL}")
        print(f"[Gemini TTS Cloud GA] Payload: {json.dumps(payload, indent=2)}")
        response = requests.post(CLOUD_TTS_URL, json=payload, headers=headers, timeout=400)
        print(f"[Gemini TTS Cloud GA] Response status: {response.status_code}")
        if response.status_code != 200:
            print(f"[Gemini TTS Cloud GA] Response body: {response.text[:500]}")
        response.raise_for_status()
        data = response.json()

        # Cloud TTS returns base64 audio in audioContent field
        audio_content_b64 = data.get("audioContent")
        if not audio_content_b64:
            raise HTTPException(status_code=500, detail="No audio content in Cloud TTS response")

        audio_data = base64.b64decode(audio_content_b64)

        # LINEAR16 is already PCM, convert to WAV
        sample_rate = 24000
        sample_width = 2  # 16-bit
        num_channels = 1  # Mono

        wav_bytes = pcm_to_wav(audio_data, sample_width, sample_rate, num_channels)
        print(f"[Gemini TTS Cloud GA] Successfully generated {len(wav_bytes)} bytes of audio")
        return wav_bytes

    except requests.exceptions.RequestException as e:
        error_detail = str(e)
        try:
            error_detail = e.response.json().get("error", {}).get("message", e.response.text)
        except Exception:
            pass
        print(f"ERROR: Cloud TTS API error: {error_detail}")
        raise HTTPException(status_code=e.response.status_code if e.response else 502, detail=f"Cloud TTS API error: {error_detail}")
    except Exception as e:
        print(f"ERROR: Cloud TTS audio conversion failed: {e}")
        raise HTTPException(status_code=500, detail=f"Cloud TTS audio conversion failed: {e}")


