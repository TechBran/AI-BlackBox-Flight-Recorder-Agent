def generate_prompt_slug(prompt: str, max_length: int = 30) -> str:
    """Generate URL-safe slug from prompt for human/model readable filenames.

    Args:
        prompt: The generation prompt
        max_length: Maximum length of slug (default 30)

    Returns:
        URL-safe slug string (e.g., "sunset-over-mountains")
    """
    import unicodedata

    # Remove common prefixes that don't add value to the filename
    prompt_lower = prompt.lower()
    prefixes = [
        'generate ', 'create ', 'make ', 'draw ', 'paint ',
        'an image of ', 'a photo of ', 'a picture of ',
        'an image showing ', 'a video of ', 'a video showing ',
        'music for ', 'a song about ', 'audio of '
    ]
    for prefix in prefixes:
        if prompt_lower.startswith(prefix):
            prompt = prompt[len(prefix):]
            break

    # Normalize unicode and convert to ASCII
    slug = unicodedata.normalize('NFKD', prompt).encode('ascii', 'ignore').decode('ascii')

    # Convert to lowercase and replace non-alphanumeric with spaces
    slug = re.sub(r'[^a-z0-9\s]', '', slug.lower())

    # Replace multiple spaces with single hyphen
    slug = re.sub(r'\s+', '-', slug.strip())

    # Truncate at word boundary if too long
    if len(slug) > max_length:
        # Find last hyphen before max_length
        truncated = slug[:max_length]
        last_hyphen = truncated.rfind('-')
        if last_hyphen > 10:  # Only truncate at word boundary if we have enough chars
            slug = truncated[:last_hyphen]
        else:
            slug = truncated

    # Remove trailing hyphens and ensure we have something
    slug = slug.rstrip('-')

    return slug if slug else 'media'


