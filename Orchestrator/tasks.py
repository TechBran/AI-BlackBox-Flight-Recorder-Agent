def process_image_generation(task: Task):
    """Generate image(s) using Google Imagen (Nano Banana Pro)

    Supports multiple images, aspect ratios, resolutions, and reference images.
    Options are stored in task.result_data["options"] by the endpoint.
    """
    update_task(task.task_id, progress=30)

    # Extract options from task.result_data
    options = {}
    if task.result_data and "options" in task.result_data:
        options = task.result_data["options"]
        print(f"[WORKER] Image generation options: {options.get('aspectRatio', '16:9')} @ {options.get('resolution', '1K')}, count={options.get('numberOfImages', 1)}")

    # Route to a per-provider adapter; untagged tasks -> default provider (back-compat)
    provider = (options.get("provider") or _default_image_provider())
    fn = IMAGE_PROVIDERS.get(provider) or IMAGE_PROVIDERS[DEFAULT_IMAGE_PROVIDER]
    print(f"[WORKER] Image provider: {provider}")
    images = fn(task.prompt, options)

    # Provider-aware provenance: record the model + the params that ACTUALLY
    # apply for the routed provider (not gemini's defaults regardless). openai
    # bills size/quality; grok/gemini use aspectRatio; gemini also has resolution.
    _IMAGE_MODELS = {
        "gemini": GOOGLE_IMAGEN_MODEL,
        "openai": OPENAI_IMAGE_MODEL,
        "grok": XAI_IMAGE_MODEL,
    }
    recorded_model = _IMAGE_MODELS.get(provider, GOOGLE_IMAGEN_MODEL)
    _num_images = options.get("numberOfImages", 1)
    if provider == "openai":
        image_metadata = {
            "size": options.get("size", "auto"),
            "quality": options.get("quality", "auto"),
            "numberOfImages": _num_images,
            "model": recorded_model,
        }
    elif provider == "grok":
        image_metadata = {
            "aspect_ratio": options.get("aspectRatio", "16:9"),
            "numberOfImages": _num_images,
            "model": recorded_model,
        }
    else:  # gemini (default)
        image_metadata = {
            "aspect_ratio": options.get("aspectRatio", "16:9"),
            "resolution": options.get("resolution", "1K"),
            "numberOfImages": _num_images,
            "model": recorded_model,
        }

    update_task(task.task_id, progress=70)

    # Save all generated images with prompt-based slug filename
    slug = generate_prompt_slug(task.prompt)
    urls = []
    for i, image_bytes in enumerate(images):
        if len(images) == 1:
            filename = f"{slug}_{task.task_id}.png"
        else:
            filename = f"{slug}_{task.task_id}_{i+1}.png"
        save_path = UPLOADS_DIR / filename
        save_path.write_bytes(image_bytes)
        url = f"/ui/uploads/{filename}"
        urls.append(url)
        print(f"[WORKER] Image {i+1} saved: {filename}")

        # Index the media file for search/list tools
        add_media_entry(
            url=url,
            media_type="image",
            prompt=task.prompt,
            task_id=task.task_id,
            filename=filename,
            file_size=len(image_bytes),
            extra_metadata=dict(image_metadata)
        )

    # Use first URL as primary result, store all URLs in result_data
    primary_url = urls[0] if urls else None
    result_data = task.result_data or {}
    result_data["all_urls"] = urls

    # Create artifact metadata for snapshot index
    artifact = {
        "type": "image",
        "url": primary_url,
        "all_urls": urls,
        "task_id": task.task_id,
        "prompt": task.prompt,
        "model": recorded_model,
        "aspect_ratio": image_metadata.get("aspect_ratio"),
        "resolution": image_metadata.get("resolution"),
        "created_at": now_utc_iso()
    }
    result_data["artifact"] = artifact

    update_task(task.task_id,
               status=TaskStatus.COMPLETED,
               result_url=primary_url,
               result_data=result_data,
               progress=100)
    print(f"[WORKER] Generated {len(urls)} image(s): {urls}")

def process_video_generation(task: Task):
    """Generate video using Veo 3.1 (text-to-video, image-to-video, or video extension)"""
    update_task(task.task_id, progress=20)

    if not GOOGLE_API_KEY:
        raise HTTPException(status_code=400, detail="GOOGLE_API_KEY not configured")

    # Step 1: Initiate video generation
    base_url = "https://generativelanguage.googleapis.com/v1beta"
    predict_url = f"{base_url}/models/{GOOGLE_VEO_MODEL}:predictLongRunning"

    # Build instances payload
    instance = {"prompt": task.prompt}

    # Check if this is a video extension (video URL in result_data)
    # WebSocket handlers wrap in {"options": {...}}, REST handlers use flat structure
    # Need to check both formats: direct keys AND nested in "options"
    video_url_for_extension = None
    if task.result_data:
        # Check direct keys first (REST handlers, /extend/video endpoint)
        if "url" in task.result_data:
            video_url_for_extension = task.result_data["url"]
        elif "video_url" in task.result_data:
            video_url_for_extension = task.result_data["video_url"]
        # Check wrapped format (WebSocket handlers use {"options": {...}})
        elif "options" in task.result_data and isinstance(task.result_data["options"], dict):
            opts = task.result_data["options"]
            if "url" in opts:
                video_url_for_extension = opts["url"]
            elif "video_url" in opts:
                video_url_for_extension = opts["video_url"]

    if video_url_for_extension:
        video_url = video_url_for_extension
        print(f"[WORKER] Video extension mode: extending {video_url}")

        # Extract path from full URL if it's a Tailscale URL
        if video_url.startswith("http://") or video_url.startswith("https://"):
            from urllib.parse import urlparse
            parsed = urlparse(video_url)
            video_url = parsed.path
            print(f"[WORKER] Extracted path from URL: {video_url}")

        # Look up the original Google video URI for extensions
        if video_url.startswith("/ui/uploads/"):
            # Extract task_id from filename
            # Handles both old format ({task_id}.mp4) and new slug format ({slug}_{task_id}.mp4)
            filename = video_url.split("/")[-1]
            base_name = filename.replace(".mp4", "")

            # Check if it's the new slug format (contains underscore with UUID after it)
            # UUID format: 8-4-4-4-12 hex chars (36 chars with dashes)
            if "_" in base_name:
                # Try to extract UUID from the end (after last underscore that precedes a UUID)
                parts = base_name.rsplit("_", 1)
                potential_uuid = parts[-1] if len(parts) > 1 else base_name
                # Check if it looks like a UUID (36 chars with dashes in right places)
                if len(potential_uuid) == 36 and potential_uuid[8] == "-" and potential_uuid[13] == "-":
                    original_task_id = potential_uuid
                else:
                    original_task_id = base_name  # Fallback to full name
            else:
                original_task_id = base_name

            # Look up the original task to get the Google video URI
            original_task = task_db.get_task(original_task_id)

            if original_task and original_task.google_video_uri:
                # Use the stored Google video URI for extension
                instance["video"] = {
                    "uri": original_task.google_video_uri
                }
                print(f"[WORKER] Using stored Google video URI for extension: {original_task.google_video_uri}")
            else:
                # Fallback: No stored URI found (shouldn't happen for Veo-generated videos)
                raise Exception(
                    f"Cannot extend video: No Google video URI found for task {original_task_id}. "
                    f"Video extensions only work with videos generated by Veo."
                )
        else:
            # External URL - use as-is (likely won't work, but try anyway)
            instance["video"] = {
                "uri": video_url
            }
            print(f"[WORKER] Using external video URI: {video_url}")
    # Add image if provided (for image-to-video)
    elif task.image_data:
        instance["image"] = {
            "bytesBase64Encoded": task.image_data["data"],
            "mimeType": task.image_data["mime_type"]
        }
        print(f"[WORKER] Image-to-video mode: animating uploaded image")
    # Check for image_url in result_data (for model-initiated image-to-video)
    # Note: We check both wrapped {"options": {...}} and direct format
    else:
        image_url = None
        if task.result_data:
            raw_data = task.result_data
            if isinstance(raw_data, dict):
                wrapped_opts = raw_data.get("options", {})
                if isinstance(wrapped_opts, dict):
                    image_url = wrapped_opts.get("image_url")
                if not image_url:
                    image_url = raw_data.get("image_url")

        if image_url:
            print(f"[WORKER] Image-to-video mode via URL: {image_url}")

            # Clean URL - remove host prefix if present
            clean_url = image_url
            if clean_url.startswith("http://") or clean_url.startswith("https://"):
                from urllib.parse import urlparse
                parsed = urlparse(clean_url)
                clean_url = parsed.path

            # Resolve to file path
            if clean_url.startswith("/ui/"):
                relative_path = clean_url.replace("/ui/", "")
                file_path = Path("Portal") / relative_path
            else:
                raise Exception(f"Invalid image URL format: {image_url}")

            if not file_path.exists():
                raise Exception(f"Image file not found: {file_path}")

            # Read and encode image
            import base64
            with open(file_path, "rb") as f:
                image_bytes = f.read()

            # Detect MIME type
            suffix = file_path.suffix.lower()
            mime_map = {".png": "image/png", ".jpg": "image/jpeg", ".jpeg": "image/jpeg", ".webp": "image/webp", ".gif": "image/gif"}
            mime_type = mime_map.get(suffix, "image/jpeg")

            instance["image"] = {
                "bytesBase64Encoded": base64.b64encode(image_bytes).decode("utf-8"),
                "mimeType": mime_type
            }
            print(f"[WORKER] Loaded image from {file_path} ({mime_type}, {len(image_bytes)} bytes)")
        else:
            print(f"[WORKER] Text-to-video mode")

    # Extract video options from result_data (if not a video extension)
    # Handle both formats: direct options OR wrapped in {"options": ...}
    raw_result_data = task.result_data or {}
    if "options" in raw_result_data and isinstance(raw_result_data["options"], dict):
        video_options = raw_result_data["options"]
    else:
        video_options = raw_result_data
    is_extension = "url" in raw_result_data or "video_url" in raw_result_data or "url" in video_options or "video_url" in video_options

    # Get aspect ratio and resolution from options
    aspect_ratio = video_options.get("aspectRatio", "16:9")
    resolution = video_options.get("resolution", "720p")
    duration = video_options.get("duration", 8)
    negative_prompt = video_options.get("negativePrompt")

    # Validate 1080p + 8s constraint
    if resolution == "1080p" and duration != 8:
        print(f"[WORKER] Warning: 1080p requires 8s duration. Falling back to 720p.")
        resolution = "720p"

    print(f"[WORKER] Video options: {aspect_ratio}, {duration}s, {resolution}")

    # Build parameters
    parameters = {
        "sampleCount": 1,
        "aspectRatio": aspect_ratio,
        "resolution": resolution
    }

    # Only add durationSeconds for new video generation (not for extensions)
    if not is_extension:
        parameters["durationSeconds"] = duration
        print(f"[WORKER] Duration: {duration} seconds")

    # Add negative prompt if provided (appended to prompt for filtering)
    actual_prompt = task.prompt
    if negative_prompt and not is_extension:
        # Note: Veo doesn't have native negative prompt, but we can append "without: ..."
        actual_prompt = f"{task.prompt}. Avoid: {negative_prompt}."
        instance["prompt"] = actual_prompt
        print(f"[WORKER] Negative prompt appended: {negative_prompt}")

    payload = {
        "instances": [instance],
        "parameters": parameters
    }
    
    print(f"[WORKER] Initiating Veo 3.1 video generation...")
    response = requests.post(
        predict_url,
        headers={
            "x-goog-api-key": GOOGLE_API_KEY,
            "Content-Type": "application/json"
        },
        json=payload,
        timeout=30
    )

    # Log error details if request failed
    if response.status_code != 200:
        try:
            error_data = response.json()
            print(f"[WORKER] API Error Response: {error_data}")
        except:
            print(f"[WORKER] API Error (no JSON): {response.text[:500]}")

    response.raise_for_status()
    data = response.json()
    
    operation_name = data.get("name")
    if not operation_name:
        raise Exception("No operation name returned from Veo API")
    
    print(f"[WORKER] Operation started: {operation_name}")
    update_task(task.task_id, progress=30)
    
    # Step 2: Poll for completion
    poll_url = f"{base_url}/{operation_name}"
    max_attempts = 120  # 20 minutes max (10 second intervals)
    attempt = 0
    
    while attempt < max_attempts:
        time.sleep(10)  # Poll every 10 seconds
        attempt += 1
        
        # Update progress gradually
        progress = min(30 + (attempt * 0.5), 80)
        update_task(task.task_id, progress=int(progress))
        
        poll_response = requests.get(
            poll_url,
            headers={"x-goog-api-key": GOOGLE_API_KEY},
            timeout=30
        )
        poll_response.raise_for_status()
        poll_data = poll_response.json()
        
        if poll_data.get("done"):
            print(f"[WORKER] Video generation complete!")
            update_task(task.task_id, progress=85)

            # Step 3: Check for RAI filtering (content policy rejection)
            generate_response = poll_data.get("response", {}).get("generateVideoResponse", {})
            if "raiMediaFilteredCount" in generate_response:
                filtered_count = generate_response.get("raiMediaFilteredCount", 0)
                filtered_reasons = generate_response.get("raiMediaFilteredReasons", [])
                reason_str = "; ".join(filtered_reasons) if filtered_reasons else "Content policy violation"
                raise Exception(f"Video blocked by Google content policy ({filtered_count} filtered): {reason_str}")

            # Step 4: Extract video URI and download
            try:
                video_uri = generate_response["generatedSamples"][0]["video"]["uri"]
            except (KeyError, IndexError) as e:
                raise Exception(f"Could not extract video URI from response: {poll_data}")
            
            # Download the video
            print(f"[WORKER] Downloading video from: {video_uri}")
            video_response = requests.get(
                video_uri,
                headers={"x-goog-api-key": GOOGLE_API_KEY},
                timeout=300,
                stream=True
            )
            video_response.raise_for_status()
            
            # Save video with prompt-based slug filename
            slug = generate_prompt_slug(task.prompt)
            filename = f"{slug}_{task.task_id}.mp4"
            save_path = UPLOADS_DIR / filename
            with open(save_path, 'wb') as f:
                for chunk in video_response.iter_content(chunk_size=8192):
                    f.write(chunk)

            url = f"/ui/uploads/{filename}"

            # Index the media file for search/list tools
            file_size = save_path.stat().st_size if save_path.exists() else None
            add_media_entry(
                url=url,
                media_type="video",
                prompt=task.prompt,
                task_id=task.task_id,
                filename=filename,
                file_size=file_size,
                extra_metadata={
                    "aspect_ratio": aspect_ratio,
                    "resolution": resolution,
                    "duration": duration,
                    "model": GOOGLE_VEO_MODEL,
                    "google_video_uri": video_uri
                }
            )

            # Create artifact metadata for snapshot index
            result_data = task.result_data or {}
            artifact = {
                "type": "video",
                "url": url,
                "task_id": task.task_id,
                "prompt": task.prompt,
                "model": GOOGLE_VEO_MODEL,
                "aspect_ratio": aspect_ratio,
                "resolution": resolution,
                "duration": duration,
                "google_video_uri": video_uri,
                "created_at": now_utc_iso()
            }
            result_data["artifact"] = artifact

            update_task(task.task_id,
                       status=TaskStatus.COMPLETED,
                       result_url=url,
                       result_data=result_data,
                       progress=100,
                       google_video_uri=video_uri)  # Store original Google URI for extensions
            print(f"[WORKER] Video saved: {url}")
            print(f"[WORKER] Stored Google video URI for extensions: {video_uri}")
            return
        
        if poll_data.get("error"):
            error_msg = poll_data["error"].get("message", "Unknown error")
            raise Exception(f"Veo API error: {error_msg}")
    
    raise Exception("Video generation timed out after 20 minutes")

def process_audio_analysis(task: Task):
    """Analyze audio using Gemini"""
    update_task(task.task_id, progress=20)
    
    if not GOOGLE_API_KEY:
        raise HTTPException(status_code=400, detail="GOOGLE_API_KEY not configured")
    
    # Step 1: Upload audio file to Gemini Files API
    file_path = Path(task.input_file)
    if not file_path.exists():
        raise Exception(f"Audio file not found: {task.input_file}")
    
    print(f"[WORKER] Uploading audio file to Gemini Files API...")
    
    # Determine MIME type using standard MIME types
    # Based on Gemini API examples at https://ai.google.dev/api/files
    mime_type_map = {
        ".mp3": "audio/mpeg",   # Standard MIME type for MP3
        ".wav": "audio/wav",
        ".m4a": "audio/mp4",    # M4A is MPEG-4 audio container
        ".mp4": "audio/mp4",
        ".aac": "audio/aac",
        ".ogg": "audio/ogg",
        ".flac": "audio/flac",
        ".aiff": "audio/aiff"
    }
    mime_type = mime_type_map.get(file_path.suffix.lower(), "audio/mpeg")

    print(f"[WORKER] File: {file_path.name}, Extension: {file_path.suffix.lower()}, MIME type: {mime_type}")

    # Read file
    audio_bytes = file_path.read_bytes()
    num_bytes = len(audio_bytes)

    print(f"[WORKER] File size: {num_bytes} bytes")

    # Initial resumable upload request
    print(f"[WORKER] Starting resumable upload to Gemini Files API...")
    upload_url_response = requests.post(
        "https://generativelanguage.googleapis.com/upload/v1beta/files",
        headers={
            "x-goog-api-key": GOOGLE_API_KEY,
            "X-Goog-Upload-Protocol": "resumable",
            "X-Goog-Upload-Command": "start",
            "X-Goog-Upload-Header-Content-Length": str(num_bytes),
            "X-Goog-Upload-Header-Content-Type": mime_type,
            "Content-Type": "application/json"
        },
        json={"file": {"display_name": file_path.name}},
        timeout=30
    )

    # Check for errors with detailed logging
    if upload_url_response.status_code != 200:
        error_body = upload_url_response.text
        print(f"[WORKER] ERROR: Upload initiation failed with status {upload_url_response.status_code}")
        print(f"[WORKER] ERROR: Response body: {error_body}")
        raise Exception(f"Gemini Files API returned {upload_url_response.status_code}: {error_body}")

    upload_url_response.raise_for_status()
    upload_url = upload_url_response.headers.get("x-goog-upload-url")
    
    if not upload_url:
        raise Exception("No upload URL received from Gemini Files API")
    
    update_task(task.task_id, progress=40)
    
    # Upload the actual file bytes
    print(f"[WORKER] Uploading {num_bytes} bytes...")
    upload_response = requests.post(
        upload_url,
        headers={
            "Content-Length": str(num_bytes),
            "X-Goog-Upload-Offset": "0",
            "X-Goog-Upload-Command": "upload, finalize"
        },
        data=audio_bytes,
        timeout=300
    )
    upload_response.raise_for_status()
    file_info = upload_response.json()
    file_uri = file_info["file"]["uri"]
    
    print(f"[WORKER] File uploaded: {file_uri}")
    update_task(task.task_id, progress=60)
    
    # Step 2: Generate content with Gemini
    prompt = task.result_data.get("prompt") if task.result_data else None
    if not prompt:
        prompt = "Generate a detailed transcript with timestamps, identify speakers if multiple people are present, and analyze the emotional tone and key topics discussed in this audio."
    
    generate_url = f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent"
    generate_payload = {
        "contents": [{
            "parts": [
                {"text": prompt},
                {"file_data": {"mime_type": mime_type, "file_uri": file_uri}}
            ]
        }]
    }
    
    print(f"[WORKER] Analyzing audio with Gemini...")
    generate_response = requests.post(
        generate_url,
        headers={
            "x-goog-api-key": GOOGLE_API_KEY,
            "Content-Type": "application/json"
        },
        json=generate_payload,
        timeout=120
    )
    generate_response.raise_for_status()
    analysis = generate_response.json()
    
    update_task(task.task_id, progress=90)
    
    # Extract text response
    try:
        text_result = analysis["candidates"][0]["content"]["parts"][0]["text"]
    except (KeyError, IndexError):
        text_result = json.dumps(analysis, indent=2)
    
    # Save result
    result_data = {
        "transcript": text_result,
        "file_uri": file_uri,
        "analysis": analysis
    }
    
    update_task(task.task_id,
               status=TaskStatus.COMPLETED,
               result_data=result_data,
               progress=100)
    print(f"[WORKER] Audio analysis complete")

