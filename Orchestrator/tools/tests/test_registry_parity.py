"""Parity test for the ToolVault v2 tool_registry cutover (Task 4.1).

`golden_tool_schemas.json` is the "before" snapshot: every provider converter's
output, filtered to drop UGV tools and sorted by name, captured while
``tool_registry.TOOL_DEFINITIONS`` was still the giant literal list.

After the cutover (TOOL_DEFINITIONS sourced from ``registry.load_canonical()``),
these tests assert that every provider sees BYTE-IDENTICAL schemas for all
non-UGV tools, that UGV has disappeared from the surface (intended — the module
registry has no ugv tools), and that alias/executor resolution still works.

The golden was generated by ``_capture_golden.py``; regenerate only by
re-running that script against the literal source (which no longer exists after
the cutover — so the golden is effectively frozen).

Task 4.4 — ``x-source: operators``:
The golden was captured BEFORE any module carried ``x-source``, so its
``operator`` fields are plain strings with NO ``enum``. After 4.4 wires
``"x-source": "operators"`` onto every operator-bearing module, the converter
paths (which run ``resolve_schema``) fill that field's ``enum`` from the live
operator list and STRIP the ``x-source`` marker. To prove the change is
SURGICAL — i.e. ONLY the operator enum changed and nothing else drifted — the
parity tests below NORMALIZE the current output (drop the resolver-injected
``enum`` from any property named ``operator``) and assert the normalized output
is byte-identical to the (operator-enum-free) golden. Dedicated tests then
assert the operator enum IS present (filled to a monkeypatched list) and that
the ``x-source`` marker never leaks to any provider.
"""

