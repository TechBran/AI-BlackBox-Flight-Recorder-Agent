# Filters mirror _capture_golden.py exactly (drop ugv_*, sort by name).
# ---------------------------------------------------------------------------
def _is_ugv(name: str) -> bool:
    return name.startswith("ugv_")


def _filter_anthropic(tools):
    return sorted((t for t in tools if not _is_ugv(t["name"])), key=lambda t: t["name"])


def _filter_openai_rest(tools):
    return sorted(
        (t for t in tools if not _is_ugv(t["function"]["name"])),
        key=lambda t: t["function"]["name"],
    )


def _filter_gemini(tools):
    out = []
    for wrapper in tools:
        new_wrapper = dict(wrapper)
        for key in ("function_declarations", "functionDeclarations"):
            if key in new_wrapper:
                new_wrapper[key] = sorted(
                    (d for d in new_wrapper[key] if not _is_ugv(d["name"])),
                    key=lambda d: d["name"],
                )
        out.append(new_wrapper)
    return out


def _mcp_to_dicts(tools):
    return [
        {"name": t.name, "description": t.description, "inputSchema": t.inputSchema}
        for t in tools
    ]


def _roundtrip(obj):
    """JSON round-trip so the comparison is on plain serializable structures,
    identical to how the golden was written (e.g. tuples -> lists)."""
    return json.loads(json.dumps(obj))


# ---------------------------------------------------------------------------
# Operator-enum normalization (Task 4.4).
#
# The golden predates x-source, so its `operator` properties have no `enum`.
# Current output has a resolver-injected `enum` on every property named exactly
# `operator`. Stripping just that key from the current output should make it
# byte-identical to the golden — proving NOTHING ELSE changed.
# ---------------------------------------------------------------------------
def _strip_operator_enums(obj):
    """Recursively drop the `enum` key from any dict property named `operator`.

    Walks every `properties` map (wherever it appears across the provider
    shapes) and, for the `operator` property specifically, removes the injected
    `enum`. Returns a new structure; the input is not mutated.
    """
    if isinstance(obj, list):
        return [_strip_operator_enums(v) for v in obj]
    if isinstance(obj, dict):
        out = {}
        for k, v in obj.items():
            if k == "properties" and isinstance(v, dict):
                new_props = {}
                for pname, pval in v.items():
                    if pname == "operator" and isinstance(pval, dict):
                        pval = {pk: pv for pk, pv in pval.items() if pk != "enum"}
                    new_props[pname] = _strip_operator_enums(pval)
                out[k] = new_props
            else:
                out[k] = _strip_operator_enums(v)
        return out
    return obj


def _find_xsource_paths(obj, path=""):
    """Return the JSON-ish paths of any surviving `x-source` keys (should be [])."""
    leaks = []
    if isinstance(obj, dict):
        if "x-source" in obj:
            leaks.append(path or "<root>")
        for k, v in obj.items():
            leaks.extend(_find_xsource_paths(v, f"{path}/{k}"))
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            leaks.extend(_find_xsource_paths(v, f"{path}[{i}]"))
    return leaks


