def _params_by_name_flat(tools, name_key="name", params_key=None):
    """For flat-list formats (anthropic/openai_realtime): name -> params object."""
    out = {}
    for t in tools:
        out[t[name_key]] = t.get(params_key) if params_key else t
    return out


