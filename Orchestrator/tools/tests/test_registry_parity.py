# Task 4.4 — live end-to-end: the chat injector and the MCP getter both
# resolve x-source operator fields (hermetic; operators are pinned by the
# autouse fixture).
# ---------------------------------------------------------------------------
def test_inject_for_prompt_resolves_operator():
    from Orchestrator.toolvault import injector

    # A prompt that should select search_snapshots (memory search).
    formatted, _instructions = injector.inject_for_prompt(
        "search my snapshots memory for past work on the portal",
        "anthropic",
        "chat",
    )
    by_name = {t["name"]: t for t in formatted}
    assert "search_snapshots" in by_name, "injector did not select search_snapshots"
    op = _operator_prop(by_name["search_snapshots"]["input_schema"])
    assert op is not None, "operator property missing after inject"
    assert op.get("enum") == FAKE_OPERATORS, "operator enum not filled by injector"
    assert "x-source" not in op, "injector leaked x-source"
    # And no x-source anywhere in the whole injected payload.
    assert _find_xsource_paths(formatted) == [], "injector leaked x-source somewhere"


def test_get_mcp_tools_resolves_operator():
    tools = {t.name: t for t in tr.get_mcp_tools()}
    op = _operator_prop(tools["search_snapshots"].inputSchema)
    assert op is not None and op.get("enum") == FAKE_OPERATORS
    assert "x-source" not in op
    # No x-source leaked into any MCP tool's input schema.
    leaks = []
    for t in tr.get_mcp_tools():
        leaks.extend(_find_xsource_paths(t.inputSchema, t.name))
    assert leaks == [], f"MCP leaked x-source: {leaks}"


